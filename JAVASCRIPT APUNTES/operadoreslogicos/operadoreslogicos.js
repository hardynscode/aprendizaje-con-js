

/* 
    * El operador lógico AND(&&) devolverá true cuando ambos valores que conecta son true

    * true && true // -> true
    * true && false / -> false
    * false && false / --> false
    
    * El operador lógico OR(||) devuelve true cuando cualquiera de los dos valores que conecta es true

    * true || true // -> true
    * true || false // -> true
    * false || false // -> false
    * true || false || true -> true
    
    * El operador lógico NOT (!) invierte el valor de un valor de un valor booleano, se pone al principio del valor booleano que queremos invertir 

    !true // -> este será false
    !false // -> este será true

 */